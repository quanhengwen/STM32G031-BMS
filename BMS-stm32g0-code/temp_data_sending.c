uint16_t byte2halfWord(uint8_t byte){
	//declare output 16bit variable and 8bit mask variable
	uint16_t halfWord = 0;
	uint8_t mask = 0b00000001;
	//initialise the base variable
	int base = 1;
	//for every bit in input 8bit variable, check if it is 0 or 1
	//and add the number to output variable depend on what bit in byte
	//you look for
	for(int i = 0; i < 8; i++){
		if(byte & mask){
			halfWord += base;
		}
		else{
			halfWord += 2 * base;
		}
		mask <<= 1;
		base *= 4;
	}
	return halfWord;
}

